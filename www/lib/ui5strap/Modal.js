sap.ui.define(['./library', './ControlBase'], function(library, ControlBase){

	var Modal = ControlBase.extend("ui5strap.Modal", {
		metadata : {
			deprecated : true,
			// ---- object ----
			defaultAggregation : "body",
			// ---- control specific ----
			library : "ui5strap",
			properties : { 
				animate : {
          type:"boolean", 
          defaultValue:true
        },
        backdrop : {
          type:"boolean", 
          defaultValue:false
        }
			},
			aggregations : { 
				header : {
					singularName: "header"
				},
				body : {
					singularName: "body"
				},
				footer : {
					singularName: "footer"
				}
			},
      events : {
         shown : {},
         hidden : {}
      }

		}
	});

	ui5strap.Modal.prototype.onAfterRendering = function(){
    var _this = this,
        $modal = this.$(),
        modalOptions = {
          show : false,
          backdrop: this.getBackdrop()
        };

    window.setTimeout(function(){
        jQuery(document.body).append($modal.detach());
        
        $modal
          .modal(modalOptions)
          .on('hidden.bs.modal', function(){
              _this.fireHidden();
          })
          .on('shown.bs.modal', function(){
              _this.fireShown();
          });
    }, 250);
  };

	ui5strap.Modal.prototype.show = function(){
		this.$().modal('show');
	};

  ui5strap.Modal.prototype.hide = function(){
    this.$().modal('hide');
  };

  ui5strap.Modal.prototype.toggle = function(){
    this.$().modal('toggle');
  };

  return Modal;
});

